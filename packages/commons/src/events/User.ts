import { z } from 'zod'

export const User = z.object({
  id: z.string(),
  name: z.array(
    z.object({
      use: z.string(),
      given: z.array(z.string()),
      family: z.string()
    })
  ),
  role: z.string(),
  signatureFilename: z.string().optional()
})

export type User = z.infer<typeof User>
